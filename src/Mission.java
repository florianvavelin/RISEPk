
import java.util.*;

/**
 * 
 */
public class Mission {

    public String name;

    /**
     * Default constructor
     */
    public Mission(String name) {
        this.name = name;
    }



    /**
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return
     */
    public String getName() {
        return this.name;
    }

}